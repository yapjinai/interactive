function playclip01() {
	if (navigator.appName == "Microsoft Internet Explorer" && (navigator.appVersion.indexOf("MSIE 7")!=-1) || (navigator.appVersion.indexOf("MSIE 8")!=-1)) {
		if (document.all)
			 {
			  document.all.sound.src = "03-01.wav";
			 }
		}

		else {
			{
			var audio = document.getElementById("03-01");
			audio.play();
			}
	}
}

